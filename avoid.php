            <h1>How to avoid disinformation</h1>
            <li>•   Nowadays, more and more people are getting news from social media instead of traditional news sources. Social media is not always a reliable source of information because anyone can share anything they want there. In addition, a study by American Press Institute has shown that people have the tendency to believe any news from people they respect, regardless of whether that news is true or not. It is better to get your news more from reputable new sites than on social media. If someone you know and/or respect share news, check from other sources to see whether the news is correct.</li>
            <li>•   Fully read news articles you see and not just the headline: Media and new sites are known to make sensational headlines in order to attract readers. These headlines mispresent the actual facts in the news in order to make the new article more attractive to viewers, but someone who only reads title will be easily misled.</li>
            <li>•   See if you are having a strong emotional reaction to what you are reading. News sites often embellish stories in order to elicit a strong emotional response because emotional people are likely to click on the article and/or believe in what the site is telling them. If this happen to you, try to learn more about the subject matter in the article from other sources to see how much of it is correct.</li> 
            <li>•   Verify your sources carefully. Not every website is reliable. 
                 <ul>
                    <li>o   Check the domain name to make sure that it is the new sites you are looking for and not an imposter. Imposter sites often have similar name to a real site, but with a .co domain suffix.</li>
                    <li>o   Check the web design and grammatical spelling of the site. Bad design and poor grammar may be indicative of a low-quality and untrustworthy site.</li>
                    <li>o   Make sure that the site you are seeing is not a satire site. Satire sites are sites that intentionally post made-up stories for entertainment purpose. Usually, the site will have a notice that states that everything there are fictional. However, many people often fail to see the notice and believe that they are reading real stories. What is more dangerous is that some sites will take the information in these satire sites as facts and repose them back in their own sites as factual news.</li>
                    <li>o   Check to see if the news you read in a site has been covered by any other sites before. Unless the site is a local news site, other sites will often cover the same story. This can be done by searching for similar keywords on online search engines.</li>
                </ul>
            </li>
            <li>•   The most accurate source of information is usually firsthand sources. Firsthand sources come directly from the people involved in the event covered by the news. Some of the firsthand sources include legal documents, leaked documents, interviews, quotes, and press releases. Reputable sources tend to show these firsthand sources to prove the legitimacy of their information. Instead of reading a summary of the event as told by the news sources, you read about the event directly from firsthand sources included in the news article.</li>       
            <li>•   It is a common occurrence that many news sites do not perform firsthand reporting, but rather report news from other sites. During this process, some pieces of information may be lost or omitted, which can lead to misinformation. To counter this, look for the first new sites that cover the event. They often contain the most complete amount of information.</li>
            <li>•   It is always a good practice to get news from more than one source. One source may omit some information that other sources have. Reading news from multiple sources can give you a more complete picture. </li>
            <li>•   Do not search only for what is frequently mentioned on the news. Search for what is not mentioned on the news. For instance, during the Democratic candidacy selection of 2020, Democratic candidate Bernie Sanders is mentioned far more frequently on Reddit than candidate Joe Biden, despite the fact that Biden actually holds far more delegations than Sanders. Someone who does not keep track on all candidates may be misled into thinking that Bernie is the more popular of the two.</li>
