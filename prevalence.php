                    <h5>According to the Flash Eurobarometer 464 report done by the European commission, of the 26,576 people surveyed, 37% of the respondents encounter fake news almost every day, with a further 31% encountering fake news at least once a week. Only a minority of people believe they seldom ever encounter fake news.<br>
                        According to (Westgarth, 2019), misinformation about vaccine is very common in the UK. When the Royal Society for Health interviewed parents in January 2019, it was reported that 41% of parents surveyed had been exposed to vaccine misinformation, 50% in parents with children under two years old. Facebook, according to Westgarth, is a major contributor to vaccine misinformation. Many individuals and groups may buy ad space on Facebook that promotes wrongful ideas about Vaccine. Citing the Daily Beast, Westgarth states that anti-vaccine ads have been viewed between 1.6 and 5.2 million times, mainly by women age 25 who are likely to have children. As a result of this misinformation campaign, the vaccination rate in England decreases.<br>
                        In this digital age, whoever controls the flow of information can control public perception. As a result, many people are attempting to put wrongful information on popular websites in order to bring the public to their side. Even media giants like Twitter and YouTube are not immune to this act. According to (Avazz Report, 2020), 16% of the top 100 YouTube videos on “global warming” included under related and up-next bar contain misinformation. This means that after you watch a normal climate change video, the next video on your recommendation list has a chance to be a misinformation video. It is discovered in the report that misinformation videos are able to reach millions of views. In total, Avazz estimates that all of the videos they have reviewed add up to 21 million views, but believes that the number of videos they have not reviewed will add up more to a total of hundreds of millions of views. The YouTube’s algorithm and advertisement program play a significant role aiding the spread of misinformation. After you watch a video, YouTube is likely to recommend similar videos to you. It is estimated that 70% of the time a person spends on YouTube is on videos recommended by YouTube. Due to this, you will get more misinformation videos after watching a single one. The YouTube advertisement program does not properly check the videos to see if they contain misinformation before allows ads to run on them. As a result, climate change misinformation video creators are incentivized to keep on making more deceptive content because YouTube still allows advertisement on their videos. Due to YouTube’s tremendous reach, misinformation spread on the site can spread very quickly, causing possibly up to hundreds of millions to receive wrongful information.<br>
